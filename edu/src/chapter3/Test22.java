package chapter3;


public class Test22 {

	public static void main(String[] args) {
		int a = 12;
		int b = 2;
		char op = '+';
		
		switch(op) {
		case '+':
			System.out.println(a + b);
		case '-':
			System.out.println(a - b);
		case '*':
			System.out.println(a * b);
		case '/':
			System.out.println(a / b);
			
		}
			

	}

}

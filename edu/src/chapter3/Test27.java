package chapter3;

public class Test27 {

	public static void main(String[] args) {
		for (int i=10; i<10; i++) {
			System.out.println("Ok");
		}
		int j = 10;
		while(j<10){
			System.out.println("while ok");
			j++;
		}
		
		int k = 10;
		do {
			System.out.println("do-while ok");
			k++;
		} while(k<10);
	}
}
